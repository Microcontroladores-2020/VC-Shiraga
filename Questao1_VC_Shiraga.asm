DIGB EQU 0x31
DIGC EQU 0x32
DIGD EQU 0x33
DIGE EQU 0x34
DIGF EQU 0x35

INICIA:
	CLRF DIGA
	CLRF DIGB
	CLRF DIGC
	CLRF DIGD
	CLRF DIGE
	CLRF DIGF
	MOVLW TRISB
	MOVWF FSR
	CLRF INDF
	MOVLW TRISC
	MOVWF FSR
	CLRF INDF
	MOVLW 0x00
	MOVWF PORTC

ADICIONA:	
	INCF DIGA, F
	MOVLW 10
	SUBWF DIGA, W
	BTFSS STATUS,Z
	GOTO ADICIONA
	CLRF DIGA
	
	INCF DIGB, F
	MOVLW 10
	SUBWF DIGB, W
	BTFSS STATUS,Z
	GOTO ADICIONA
	CLRF DIGB
	
	INCF DIGC, F
	MOVLW 10
	SUBWF DIGC, W
	BTFSS STATUS,Z
	GOTO ADICIONA
	CLRF DIGC
	
	INCF DIGD, F
	MOVLW 10
	SUBWF DIGD, W
	BTFSS STATUS,Z
	GOTO ADICIONA
	CLRF DIGD

	INCF DIGE, F
	MOVLW 10
	SUBWF DIGE,W
	BTFSS STATUS,Z
	GOTO ADICIONA
	CLRF DIGE
	
	INCF DIGF, F
	MOVLW 10
	SUBWF DIGF, W
	BTFSS STATUS,Z
	GOTO ADICIONA
	CLRF DIGF

MOSTRA:
	MOVFW DIGA
	CALL DIG
	
	MOVWF PORTB
	BSF PORTC, 0
	BCF PORTC,0	
	MOVFW DIGB
	CALL DIG
	
	MOVWF PORTB
	BSF PORTC, 1
	BCF PORTC, 1	
	MOVFW DIGC
	CALL DIG
	
	MOVWF PORTB
	BSF PORTC, 2
	BCF PORTC,2	
	MOVFW DIGD
	CALL DIG
	
	MOVWF PORTB
	BSF PORTC, 3
	BCF PORTC, 3	
	MOVFW DIGE
	CALL DIG
	
	MOVWF PORTB
	BSF PORTC, 4
	BCF PORTC, 4	
	MOVFW DIGF
	CALL DIG
	
	MOVWF PORTB
	BSF PORTC, 5
	BCF PORTC,5
	RETURN	

	MOVFW 0x22
	CALL DIG
	MOVWF PORTB
	INCF 0x22,F
	MOVLW 10
	SUBWF 0x22,W
	BTFSS STATUS,Z
	GOTO ADICIONA
FIM:
	GOTO FIM
LOOP0:		
	MOVLW 0x01    
	MOVWF PORTB   
	
LOOP:
	RLF PORTB,F   
	BTFSC PORTB,7 
	GOTO LOOP0    
	GOTO LOOP			
	
